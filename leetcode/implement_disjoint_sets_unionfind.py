class UnionFind:
    """
    Union-find Constructor  O(n)

    # QUICK FIND - O(n^2)

    Find  O(1)
    Union  O(N)
    Connected  O(1)
    """
    def __init__(self, size):
        self.root = [i for i in range(size)]

    def find(self, x):
        return self.root[x]
		
    def union(self, x, y):
        rootX = self.find(x)
        rootY = self.find(y)
        if rootX != rootY:
            for i in range(len(self.root)):
                if self.root[i] == rootY:
                    self.root[i] = rootX

    def connected(self, x, y):
        return self.find(x) == self.find(y)


